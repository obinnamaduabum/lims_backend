public class LabAccountDashboardPojo {

    private Long numberOfPatients;

    private Long totalNumberOfLabTestsOrdered;

    private Long numberOfPatientTestsOrdered;


    public Long getNumberOfPatients() {
        return numberOfPatients;
    }

    public void setNumberOfPatients(Long numberOfPatients) {
        this.numberOfPatients = numberOfPatients;
    }

    public Long getTotalNumberOfLabTestsOrdered() {
        return totalNumberOfLabTestsOrdered;
    }

    public void setTotalNumberOfLabTestsOrdered(Long totalNumberOfLabTestsOrdered) {
        this.totalNumberOfLabTestsOrdered = totalNumberOfLabTestsOrdered;
    }

    public Long getNumberOfPatientTestsOrdered() {
        return numberOfPatientTestsOrdered;
    }

    public void setNumberOfPatientTestsOrdered(Long numberOfPatientTestsOrdered) {
        this.numberOfPatientTestsOrdered = numberOfPatientTestsOrdered;
    }

}
