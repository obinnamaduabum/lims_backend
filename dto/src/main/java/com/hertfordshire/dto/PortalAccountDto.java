package com.hertfordshire.dto;

public class PortalAccountDto {

    private String name;

    private String portalAccountTypes;

    private String code;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPortalAccountTypes() {
        return portalAccountTypes;
    }

    public void setPortalAccountTypes(String portalAccountTypes) {
        this.portalAccountTypes = portalAccountTypes;
    }
}
